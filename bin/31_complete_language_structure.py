# 1. What python library means?
# -----------
# - python library can 1 or more modules like mymodule.py
# - python library can 1 or more package like mypackage
# - python library can 1 or more framework
#         -- framework is like mymodule.py/mypackage
#         -- Difference
#           In mypackage/mymodule -> We are keeping only definitions
#           BUT
#           In framework, not only definitions, some common tasks are implemented
#         -- Example: unittest framework
#           where writing test cases are different for different project
#           BUT
#           executing one by one, producing total success or failure are
#           common tasks
#           In framework these tasks are implemented.
